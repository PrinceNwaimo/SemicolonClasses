import java.util.Scanner;

public class GradeNumber {
    public static String Grade(int counter){
        if (counter==1) {
            return "first";
        }
        if (counter == 2) {
            return "second";
        }
        if (counter == 3) {
            return "third";
        }
        if (counter == 4) {
            return "fourth";
        }
        if (counter == 5) {
            return "fifth";
        }
        if (counter == 6) {
            return "sixth";
        }
        if (counter == 7) {
            return "seventh";
        }
        if (counter == 8) {
            return "eighth";
        }
        if (counter == 9) {
            return "ninth";

        }
        if (counter == 10) {
            return "tenth";
        }

        return null;
    }


    }

